export function getRegionFromCoordinates(
  xPercent: number,
  yPercent: number,
  side: Side,
): RegionKey {
  const isCenter = xPercent > 30 && xPercent < 70;
  if (side === 'front') {
    if (yPercent < 12) return 'head';
    if (yPercent < 19) return 'neck';
    if (yPercent > 60) return 'leg';
    if (!isCenter) return 'arm';
    if (yPercent < 37) return 'chest';
    if (yPercent < 54) return 'abdomen';
    return 'pelvis';
  }
  if (yPercent < 12) return 'head';
  if (yPercent < 19) return 'neck';
  if (yPercent > 60) return 'leg';
  if (!isCenter) return 'arm';
  if (yPercent < 50) return 'back';
  return 'buttocks';
}

